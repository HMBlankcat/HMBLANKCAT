#include <iostream>
using namespace std;
struct Student
{
    string name;
    int age;
    int score;
};
//打印学生信息
//1.值传递
void printStudent1(struct Student s)
{
    cout <<"子函数中 姓名"<<s.name<<"年龄"<<s.age<<"分数："<<s.score<<endl;
};
//2.地址传递
void printStudent2(struct Student *p)
{
    cout <<"子函数2中 姓名："<<p->name<<"年龄"<<p->age<<"分数"<<p->score<<endl;
};
int main()
{
//结构体做函数参数
//将学生传入到一个参数中，打印学生身上的所有信息
//创建结构体变量
    struct Student s;//给s赋值，通过.访问结构体变量中的属性
    s.name = "张三";
    s.age = 18;
    s.score = 100;
    printStudent1(s);
    printStudent2(&s);

    system("pause");
    return 0;

}//如果不想修改主函数中的数据，用值传递，反之用地址传递